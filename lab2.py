plt.hist([x / 1_000_000 for x in area], bins=30, label="Площадь стран")
plt.title("Распределение площади стран")
plt.xlabel("Площадь стран (млн. га)")
plt.ylabel("Количество стран")
plt.legend()
plt.show()

# Шапиро–Уилка
stat, p = shapiro(forest)
print("\nШапиро–Уилка")
print("H0: данные распределены нормально")
print("H1: данные НЕ распределены нормально")
print("stat =", stat, "p =", p)

if p < 0.05:
    print("Вывод: отвергаем H0 → данные НЕ нормальны")
else:
    print("Вывод: нет оснований отвергнуть H0")

# Спирмен
stat, p = spearmanr(forest, area)
print("\nСпирмен")
print("H0: корреляции нет")
print("H1: есть корреляция")
print("stat =", stat, "p =", p)

if p < 0.05:
    print("Вывод: есть статистически значимая корреляция")
else:
    print("Вывод: корреляция не обнаружена")

# Кендалл
stat, p = kendalltau(forest, area)
print("\nКендалл")
print("H0: корреляции нет")
print("H1: есть корреляция")
print("stat =", stat, "p =", p)

if p < 0.05:
    print("Вывод: есть статистически значимая корреляция")
else:
    print("Вывод: корреляция не обнаружена")

# Манна–Уитни
stat, p = mannwhitneyu(group1, group3)
print("\nМанна–Уитни")
print("H0: группы равны")
print("H1: группы различаются")
print("stat =", stat, "p =", p)

if p < 0.05:
    print("Вывод: группы различаются")
else:
    print("Вывод: различий нет")

# Краскела–Уоллиса
stat, p = kruskal(group1, group2, group3)
print("\nКраскел–Уоллис")
print("H0: все группы равны")
print("H1: есть различия")
print("stat =", stat, "p =", p)

if p < 0.05:
    print("Вывод: группы различаются")
else:
    print("Вывод: различий нет")
